export const link_service = (game_key: string) => {
    let link = null;
    switch (game_key) {
        // GAME LIST
        case "LUCKY_CARD":
            link = "";
            break;
        case "WHEEL":
            link = "";
            break; 
        case "LUCKY_BOX":
            link = "";
            break; 

        case "BOX":
            link = "";
            break; 
        case "RANDOM":
            link = "";
            break;

    }

    return link;
}
